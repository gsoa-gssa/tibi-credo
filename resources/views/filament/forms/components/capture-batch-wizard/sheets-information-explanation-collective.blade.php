<p>
    Wenn es weniger als 200 Unterschriften sind, dann zähle bitte die Bögen, gib hier das Resultat ein und auf der nächsten Seite kontrolliere, welche Bögen zurückgekommen sind. Wenn die Anzahl mit einem Versand aufgeht, reichen einige Stichproben der Bogennummern. Sonst musst du alle Bogennummern kontrollieren.
<p>
<br>
<p>
    Wenn es mehr als 200 Unterschriften sind, von einer grösseren Gemeinde, der wir vertrauen, dann prüfen wir nicht exakt, welche Bögen zurückgekommen sind, sondern können mithilfe von Angaben auf dem Brief, Angaben auf der nächsten Formularseite und von Stichproben entscheiden, welche Bögen genau zurückgekommen sind.
    <b>
        In diesem Fall wende dich aber bitte an eine verantwortliche Person, wenn irgendwas nicht aufgeht, bevor du speicherst oder viel Zeit investierst.
    </b>
